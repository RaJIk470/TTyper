/**
 * Enum class declares the words options: Commas, UpperSymbols, Digits and SpecialSymbols
 */
public enum WordsOptions {
    /**
     * option for commas after words
     */
    Commas,
    /**
     * option for upper symbols
     */
    UpperSymbols,
    /**
     * option for digits in words
     */
    Digits,
    /**
     * option for special symbols in words
     */
    SpecialSymbols
}
